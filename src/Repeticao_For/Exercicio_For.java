
package Repeticao_For;


public class Exercicio_For {

   
    public static void main(String[] args) {
        
        int x = 4;
        int y = 0;
        
        for(int i = 0; i < x; i++) {
            System.out.println(i);
            System.out.println(x);
            y = y + 10;
            System.out.println(y);
        }
    }
    
}
