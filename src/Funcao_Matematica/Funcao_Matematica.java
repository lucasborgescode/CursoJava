
package Funcao_Matematica;


public class Funcao_Matematica {

    
    public static void main(String[] args) {
        
        int x = 3;
        int y = 5;
        int z = 7;
        double a, b, c;
        
        a = Math.sqrt(x);
        b = Math.pow(y, x);
        c = Math.abs(z);
        
        System.out.println("A: " + a);
        System.out.println("B: " + b);
        System.out.println("C: " + c);
    }
    
}
